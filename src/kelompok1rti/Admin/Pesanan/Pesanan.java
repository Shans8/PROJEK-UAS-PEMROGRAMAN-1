    private void tampilkanDataPesanan() {
    try {
        Connection conn = DatabaseConnection.connect();
        String sql = "SELECT id_pesanan, id_produk, nama_produk, jumlah, total_harga, tanggal_pesanan FROM produk";
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Pesanan");
        model.addColumn("ID Produk");
        model.addColumn("Nama Produk");
        model.addColumn("Jumlah");
        model.addColumn("Total Harga");
        model.addColumn("Tanggal Pesanan");
        
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_pesanan"),
                rs.getString("id_produk"),
                rs.getString("nama_produk"),
                rs.getString("jumlah"),
                rs.getString("total_harga"),
                rs.getString("tanggal_pesanan")
            });
        }
            jTable1.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}

