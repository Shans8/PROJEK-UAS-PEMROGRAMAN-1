    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();

       if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus.");
        return;
        }

       int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus pesanan ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

       if (confirm == JOptionPane.YES_OPTION) {
        int idProduk = (int) jTable1.getValueAt(selectedRow, 0); // ambil ID di kolom 0

        try (Connection conn = DatabaseConnection.connect();
             PreparedStatement stmt = conn.prepareStatement("DELETE FROM pesanan WHERE id_pesanan = ?")) {

            stmt.setInt(1, idProduk);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Pesanan berhasil dihapus.");
            tampilkanDataPesanan(); // refresh tabel

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus pesanan: " + ex.getMessage());
            ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed

