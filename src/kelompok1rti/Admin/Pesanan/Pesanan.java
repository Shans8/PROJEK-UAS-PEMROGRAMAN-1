    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String idPesanan = jTextField1.getText().trim();
        String idProduk = jTextField2.getText().trim();
        String namaProduk = jTextField3.getText().trim();
        String jumlah = jTextField4.getText().trim();
        String totalHarga = jTextField5.getText().trim();
        String tanggalPesanan = jTextField6.getText().trim();

        if (idPesanan.isEmpty() || idProduk.isEmpty() || namaProduk.isEmpty() || jumlah.isEmpty() || totalHarga.isEmpty() || tanggalPesanan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
            return;
        }

        try (Connection conn = DatabaseConnection.connect()) {
            String sql = "INSERT INTO produk (id_pesanan, id_produk, nama_produk, jumlah, total_harga, tanggal_pesanan) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, idPesanan);
            stmt.setString(2, idProduk);
            stmt.setString(3, namaProduk);
            stmt.setString(4, jumlah);
            stmt.setString(5, totalHarga);
            stmt.setString(6, tanggalPesanan);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data pesanan berhasil disimpan.");

            // Reset form
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");

            // Tampilkan ulang data
            tampilkanDataPesanan();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

