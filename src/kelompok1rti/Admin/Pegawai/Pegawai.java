    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String idPegawai = jTextField1.getText().trim();
        String namaPegawai = jTextField2.getText().trim();
        String noPegawai = jTextField3.getText().trim();
        String jenisKelamin = jTextField4.getText().trim();
        String alamat = jTextField5.getText().trim();
        String noHp = jTextField6.getText().trim();
        String email = jTextField7.getText().trim();
        String jabatan = (String) jComboBox1.getSelectedItem();

        if (idPegawai.isEmpty() || namaPegawai.isEmpty() || noPegawai.isEmpty() || jenisKelamin.isEmpty() || alamat.isEmpty() || noHp.isEmpty() || email.isEmpty() || jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
            return;
        }
        try (Connection conn = DatabaseConnection.connect()) {
            String sql = "INSERT INTO pegawai (id_pegawai, nama_pegawai, no_pegawai, jenis_kelamin, alamat, no_hp, email, jabatan) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, idPegawai);
            stmt.setString(2, namaPegawai);
            stmt.setString(3, noPegawai);
            stmt.setString(4, jenisKelamin);
            stmt.setString(5, alamat);
            stmt.setString(6, noHp);
            stmt.setString(7, email);
            stmt.setString(8, jabatan);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data pegawai berhasil disimpan.");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
            jComboBox1.setSelectedIndex(-1);
            tampilkanDataPegawai();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String keyword = jTextField2.getText().trim();

        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan username yang ingin dicari.");
            return;
        }

        DefaultTableModel model = new DefaultTableModel(new Object[]{"ID Pegawai", "Nama Pegawai", "No Pegawai", "Jenis Kelamin", "Alamat", "No HP", "Email", "Jabatan"}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        try (Connection conn = DatabaseConnection.connect();
            PreparedStatement stmt = conn.prepareStatement("SELECT id_pegawai, nama_pegawai, no_pegawai, jenis_kelamin, alamat, no_hp, email, jabatan FROM pegawai WHERE nama_pegawai LIKE ?")) {

            stmt.setString(1, "%" + keyword + "%");
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_pegawai"),
                    rs.getString("nama_pegawai"),
                    rs.getString("no_pegawai"),
                    rs.getString("jenis_kelamin"),
                    rs.getString("alamat"),
                    rs.getString("no_hp"),
                    rs.getString("email"),
                    rs.getString("jabatan")
                });
            }
            jTable1.setModel(model);

            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "User tidak ditemukan.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mencari: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

